/**
 * PhotoGrid Background Remover - Network Capture & Analysis
 * 
 * Captures API requests when using the background remover tool
 */

const puppeteer = require('puppeteer');
const fs = require('fs').promises;

async function analyzePhotoGrid() {
  console.log('🔍 Analyzing PhotoGrid Background Remover API\n');
  console.log('=' .repeat(70));
  
  let browser;
  try {
    // Launch browser
    console.log('🌐 Launching browser...');
    browser = await puppeteer.launch({
      headless: true,
      args: ['--no-sandbox', '--disable-setuid-sandbox']
    });

    const page = await browser.newPage();
    
    // Set user agent
    await page.setUserAgent('Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36');
    
    // Enable request interception
    await page.setRequestInterception(true);

    const capturedRequests = [];
    const apiEndpoints = new Set();

    // Capture all requests
    page.on('request', request => {
      const url = request.url();
      
      // Filter for important requests
      if (url.includes('api') || 
          url.includes('remove') || 
          url.includes('background') ||
          url.includes('upload') ||
          url.includes('process') ||
          url.includes('cdn.') ||
          request.resourceType() === 'fetch' ||
          request.resourceType() === 'xhr' ||
          request.resourceType() === 'websocket') {
        
        capturedRequests.push({
          timestamp: Date.now(),
          url: url,
          method: request.method(),
          headers: request.headers(),
          postData: request.postData ? String(request.postData) : null,
          resourceType: request.resourceType()
        });
        
        try {
          const urlObj = new URL(url);
          apiEndpoints.add(urlObj.hostname);
        } catch (e) {}
        
        console.log(`\n📡 ${request.method()} ${url}`);
        if (request.postData) {
          console.log('   Body:', String(request.postData).substring(0, 300));
        }
      }
      
      request.continue();
    });

    console.log('\n📍 Navigating to PhotoGrid Background Remover...');
    
    // Navigate with longer wait
    await page.goto('https://www.photogrid.app/en/background-remover/', {
      waitUntil: 'networkidle0',
      timeout: 60000
    });

    // Wait for page to load
    console.log('⏳ Waiting for page to fully load...');
    await new Promise(resolve => setTimeout(resolve, 5000));

    // Take screenshot
    await page.screenshot({ path: 'photogrid-screenshot.png', fullPage: false });
    console.log('\n📸 Screenshot saved to: photogrid-screenshot.png');

    // Get page content
    const html = await page.content();
    await fs.writeFile('photogrid-page.html', html, 'utf-8');
    console.log('💾 Page HTML saved to: photogrid-page.html');

    // Extract JavaScript files
    const scripts = await page.evaluate(() => {
      return Array.from(document.querySelectorAll('script[src]'))
        .map(s => s.src)
        .filter(src => src.startsWith('http'));
    });
    
    console.log(`\n📄 Found ${scripts.length} external scripts`);
    scripts.forEach(src => console.log(`   ${src}`));

    // Save results
    const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
    
    const results = {
      captureTime: timestamp,
      url: 'https://www.photogrid.app/en/background-remover/',
      totalRequests: capturedRequests.length,
      apiEndpoints: Array.from(apiEndpoints),
      scripts: scripts,
      requests: capturedRequests
    };
    
    await fs.writeFile(
      `PHOTOGRID_ANALYSIS_${timestamp}.json`,
      JSON.stringify(results, null, 2),
      'utf-8'
    );
    
    // Create summary
    const summary = `# 🔍 PhotoGrid Background Remover - API Analysis

## 📊 Overview
- **Tool**: Background Remover
- **URL**: https://www.photogrid.app/en/background-remover/
- **Analysis Time**: ${timestamp}
- **Total Requests Captured**: ${capturedRequests.length}
- **API Endpoints Found**: ${apiEndpoints.size}

## 🌐 API Endpoints Detected

${Array.from(apiEndpoints).map(ep => `- ${ep}`).join('\n')}

## 📈 Key Requests

### POST Requests (Likely API Calls):
${capturedRequests.filter(r => r.method === 'POST').map(r => `- ${r.url}`).join('\n') || 'None detected'}

### Upload/Process Requests:
${capturedRequests.filter(r => r.url.includes('upload') || r.url.includes('process') || r.url.includes('remove')).map(r => `- ${r.url}`).join('\n') || 'None detected'}

## 🎯 Next Steps

1. Review the JSON file for complete request/response details
2. Test identified API endpoints directly
3. Check authentication requirements
4. Analyze payload structure for image processing

---
*Generated by PhotoGrid API Analyzer*
`;

    await fs.writeFile(
      `PHOTOGRID_ANALYSIS_${timestamp}.md`,
      summary,
      'utf-8'
    );
    
    console.log('\n✅ ANALYSIS COMPLETE!');
    console.log('=' .repeat(70));
    console.log(`\n📄 Results saved to: PHOTOGRID_ANALYSIS_${timestamp}.json`);
    console.log(`📝 Summary saved to: PHOTOGRID_ANALYSIS_${timestamp}.md`);
    console.log(`\n📊 Total requests captured: ${capturedRequests.length}`);
    console.log(`🌐 Unique API endpoints: ${apiEndpoints.size}`);

  } catch (error) {
    console.error('\n❌ Error:', error.message);
    console.error(error.stack);
  } finally {
    if (browser) {
      await browser.close();
    }
  }
}

// Run analysis
analyzePhotoGrid().catch(console.error);
